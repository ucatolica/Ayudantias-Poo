//Programa de presentacion
package prueba;

import java.util.Scanner;

//Se declara la classe Prueba
public class Prueba {
	
	//El metodo Main empieza al ejecutar la aplicación
	public static void main(String [] args){
		
		//System.out es el objeto de salida estandar en linea de comando
		// println imprime con un salto de linea
		// /n realiza un salto de linea
		System.out.print("Bienvenidos a las ayudantias de ");
		System.out.println("Programacion Orientada a Objetos");
	
		//Declaramos una variable tipo int y se le asigna un valor
		int año;
		año=2014;
		System.out.println("Semestre B\nAño "+ año);
//segunda parte
		//Se declara y se instancia un objeto tipo Scanner
		Scanner entrada = new Scanner (System.in);
		String nombre;
		System.out.print("Escriba su nombre: ");
		//Se lee una entrada
		nombre = entrada.nextLine();
		//Muestra datos con Formato
		System.out.printf("Bienvenido %s \n", nombre);

		
	}
}
